public class AsterisBox {
    public static void main(String[] args) {
        // Print the top side of the box
        System.out.println("******");
        
        // Print the sides of the box
        for (int i = 0; i < 4; i++) {
            System.out.println("*    *");
        }
        
        // Print the bottom side of the box
        System.out.println("******");
    }
}